let currentLang = localStorage.getItem('lang') || 'tr';

export function t(key) {
  return TRANSLATIONS[currentLang][key] || TRANSLATIONS['tr'][key] || key;
}

export function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (el.tagName === 'INPUT') el.placeholder = t(key);
    else el.textContent = t(key);
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  document.documentElement.lang = lang === 'tr' ? 'tr' : 'en';
  // Update meta
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) {
    metaDesc.content = lang === 'tr'
      ? 'Türkiye ve global piyasaları anlık takip edin. Kripto, borsa, döviz, hava durumu, haberler ve makroekonomik veriler.'
      : 'Track Turkey and global markets in real time. Crypto, stocks, forex, weather, news and macroeconomic data.';
  }
  document.dispatchEvent(new CustomEvent('langChange', { detail: lang }));
}

export function getLang() { return currentLang; }
